4.How to set an array in Linux?

1.IndirectDeclaration
In Indirect declaration, We assigned a value in a particular index of 
Array Variable.Noneed to first declare.
 ARRAY NAME[INDEXNR]=value
 
2.ExplicitDeclaration
In Explicit Declaration, First We declare array then assigned the 
values.
 declare -a ARRAYNAME
 
3.CompoundAssignment
In Compount Assignment, We declare array with a bunch of 
values. We can add other values later too.
 ARRAYNAME=(value1 value2 .... valueN)
